package estruturasdecontrole;

public class WhileDeterminado {

	public static void main(String[] args) {
		
		// while(expressão <- true or false até a expressão parar de rodar)
		
		int contador = 0;
			
			while (contador <= 20) {
				System.out.printf("i = %d\n", contador);
				contador+= 2;
				
				//colocar += e um número x, o código irá contar quantos x numeros tem dentro daquela condição
			//de0 a 20, +=2 (0,2, 4,...18)
			}
			
		}
		
	}